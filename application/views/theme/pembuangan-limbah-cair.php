                        <h3 class="fw-bold my-4">Jasa Pembuangan Limbah Cair</h3>

                        <p>Saat ini pembuangan limbah cair dilakukan begitu saja dan mencemari sumber air tanah, dampak kerusakannya akan menyebar luas. Untuk
                        menekan bahaya dari limbah cair, zat-zat berbahaya yang terkandung di dalamnya harus diluruhkan terlebih dahulu. Proses ini bisa memakan 
                        waktu yang cukup panjang. Dibutuhkan juga tempat khusus untuk melakukannya. Setelah limbah diolah dan tingkat racun yang ada di dalamnya
                        turun hingga titik aman, barulah pembuangan limbah cair dapat dilakukan. Bagi perusahaan, mengolah limbah seperti ini jelas mengharuskan 
                        mereka untuk berinvestasi besar dan mengeluarkan biaya operasional yang tidak sedikit.
                        Sebuah industri akan lebih mudah mengolah limbah cair
                        sebelum limbah tersebut tercampur dengan sumber air dari limbah lainnya. Pengolahan limbah cair yang dilakukan secara optimal dapat 
                        dialirkan tanpa menimbulkan dampak buruk ke badan air ataupun lingkungan sekitar.</p>

                        <h3 class="fw-bold my-4">Jasa Pengolahan Limbah Cair</h3>

                        <p>Pengelolaan limbah cair secara mandiri jelas membutuhkan biaya yang tidak sedikit. Namun jika langsung membuang limbah tanpa menjalankan
                        prosedur-prosedur yang baik dan benar, dampaknya bagi perusahaan justru jauh lebih buruk.
                        Universal Eco sebagai perusahaan pengelolaan 
                        limbah cair hadir untuk menjawab dilema yang kerap dialami oleh perusahaan. Saat jasa pengelolaan limbah cair dinilai terlalu membebani 
                        perusahaan, Universal Eco akan membantu membuat segalanya jauh lebih mudah.
                        Dalam mengolah limbah cair, Universal Eco selalu menerapkan
