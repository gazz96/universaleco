                <h3 class="fw-bold my-4">Cara Pengolahan Limbah Cair</h3>

                <p>Beberapa metode yang digunakan untuk mengolah limbah cair adalah sebagai berikut:</p>

                <ul>
                    <li>
                        <h6 class="fw-bold">Secara Fisika</h6>
                        <p>Pemisahan kontaminan dan bahan pengotor dilakukan secara fisik. Beberapa contoh proses pengolahan limbah cair secara fisika yaitu sedimentasi, absorsi, floatasi, dan penyaringan</p>
                    </li>

                    <li>
                        <h6 class="fw-bold">Secara Kimia</h6>
                        <p>Pemisahan kontaminan dan bahan pengotor dilakukan dengan menambahkan zat atau bahan kimia ke dalam limbah cair. Beberapa contoh proses pengolahan limbah cair secara kimia antara lain koagulasi, oksidasi, degradasi, penukar ion, dan ozonisasi.</p>
                    </li>

                    <li>
                        <h6 class="fw-bold">Secara Biologi</h6>
                        <p>Kontaminan atau pengotor diuraikan dengan menggunakan mikroba atau biota hidup. Beberapa contohnya antara lain yaitu proses aerobik, fakultatif, dan anaerobik.</p>
                    </li>
                </ul>
                <p>Beberapa cara dan metode di atas dilakukan agar limbah cair dapat dipastikan aman dan tidak mengandung polutan yang melewati baku mutu saat dibuang ke badan air. Baku mutu limbah cair adalah batas kadar unsur pencemar yang berada dalam limbah cair dari sebuah kegiatan usaha yang akan dilepaskan ke dalam media air.</p>

