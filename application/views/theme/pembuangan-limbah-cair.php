                <p>Setiap kegiatan industri yang menghasilkan barang dan jasa pasti menghasilkan limbah. Salah satu bentuk limbah dari kegiatan industri ini adalah bentuk cair.
                Tak beda dengan limbah dalam wujud lainnya, limbah cair dapat mengganggu keseimbangan lingkungan. Oleh karena itu, limbah atau polutan dalam bentuk cair ini tidak boleh melewati batas baku mutu yang telah ditetapkan oleh pemerintah
